package operator;

public class SignOperatorExample {

	public static void main(String[] args) {

		int x = -100;
		int result1 = +x; //-100
		int result2 = -x; //+100
		System.out.println(result1);
		System.out.println(result2);
		
		byte b = 100;
		int result3 = -b; //-100
		System.out.println(result3);

		
	}

}
