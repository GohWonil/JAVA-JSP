package ch07.sec02.exam05;

public class Parent {

	public String field1;
	
	public void method1() {
		System.out.println("parent-method1()");
	}
	
	public void method2() {
		System.out.println("parent-method2()");
	}
	
	//업캐스팅(Up Casting): 자식클래스를 부모클래스 형식으로 바꾼다
	//다운캐스팅(Down Casting): 부모클래스로 바꾼 형식을 다시 자기자신의 형식으로 바꾼다.
	
}
